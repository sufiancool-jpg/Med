export type UiTranslationKey = keyof (typeof uiTranslations)["en"];

export const isUiLanguage = (value: string): value is UiLanguage =>
  uiLanguages.some((language) => language.code === value);

export const translateUi = (
  key: UiTranslationKey,
  language: UiLanguage = defaultUiLanguage,
) => uiTranslations[language]?.[key] ?? uiTranslations[defaultUiLanguage][key];
